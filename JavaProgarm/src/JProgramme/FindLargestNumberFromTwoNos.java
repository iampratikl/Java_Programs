package JProgramme;

public class FindLargestNumberFromTwoNos {
	public static void main(String[] args) {
		int a = 70;
		int b = 100;

		if (a == b) {
			System.out.println("Both Numbers are Equals");
		} else if (a > b) {
			System.out.println(a + " Is Greater Number");
		} else {
			System.out.println(b + " Is Greater Number");
		}
	}

}
