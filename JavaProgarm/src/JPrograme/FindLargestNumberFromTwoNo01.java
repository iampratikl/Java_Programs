public class FindLargestNumberFromTwoNo01 {
	public static void main(String[] args) {

		int a = 60;
		int b = 80;

		if (a == b) {
			System.out.println("Both numbers are equal");
		} else if (a > b) {
			System.out.println(a + " is Greater number");
		} else {
			System.out.println(b + " is Greater Number");
		}

	}

}
